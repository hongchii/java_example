package java_Example;

public class Exam03 {

	public static void main(String[] args) {
		// 2개의 변수와 x,y 합과 평균을 구하라.

		int x = 63;
		int y = 18;

		System.out.println("x의 값은" + x + "입니다.");
		System.out.println("y의 값은" + y + "입니다.");
		System.out.println("x와 y의 합은" + (x + y) + "입니다.");
		System.out.println("x와 y의 평균은" + (x + y) / 2 + "입니다.");

	}

}
