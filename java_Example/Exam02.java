package java_Example;

public class Exam02 {

	public static void main(String[] args) {
		// 정수 82와 17의 합과 차를 구하라.

		int x = 82;
		int y = 17;

		System.out.println(x + y);
		System.out.println(x - y);

		System.out.println(x + " + " + y + " = " + (x + y));
		System.out.println(x + " - " + y + " = " + (x - y));
	}

}
